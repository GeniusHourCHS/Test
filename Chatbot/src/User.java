import java.util.*;
public class User {

    private String name;
    private int age;
    private String birthday;

    public User(String name) {
        this.name = name;
    }
    public User(int age) {
        this.age = age;
    }
    public User(String birthday) {
        this.birthday = birthday;
    }


    //modifiers

    //accessors

    public String getName() {
        return name;
    }
}
